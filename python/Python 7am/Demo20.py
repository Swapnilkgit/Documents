# Program to calculate total salary of employee.
# Consider basic salary as 10,000   
# 30% basic salary is hra.
# 10% basic salary is da.


salary = 10000.00
hra = salary * 0.3
da = salary * 0.1
total = salary+hra+da

print("Basic Salary = ",salary)
print(" HRA = ",hra)
print(" DA = ",da)
print("TOTAL SALARY = ",total)
